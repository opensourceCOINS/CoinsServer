	private String escapeImpl(String pValue) {
		StringBuilder sb = new StringBuilder();
		String value = StringEscapeUtils.escapeHtml4(pValue);
		Matcher matcher = mPattern.matcher(value);
		int startIndex = 0;
		while (matcher.find()) {
			sb.append(value.substring(startIndex, matcher.start()));
			startIndex = matcher.end();
			Integer num = mMap.get(matcher.group(1));
			if (num == null) {
				// Cannot replace it, then don't
				System.err.println("HTML escaper cannot find replacement for " + matcher.group(1));
				sb.append("&#");
				sb.append(matcher.group(1));
				sb.append(";");
			} else {
				sb.append("&#");
				sb.append(num);
				sb.append(";");
			}
		}
		if (startIndex > 0 && startIndex < value.length()) {
			sb.append(value.substring(startIndex));
		}
		// Some occurrences of nbsp; without & have been found...
		if (sb.length() == 0) {
			return value.replaceAll("nbsp;", "&#xA0;");
		}
		return sb.toString().replaceAll("nbsp;", "&#xA0;");
	}

	/**
	 * @param pValue
	 * @return string containing numerical values for escaped characters
	 */
	public static String escape(String pValue) {
		return getInstance().escapeImpl(pValue);
	}

	/**
	 * @param args
	 * @throws IOException
	 */
	public static void main(String... args) throws IOException {
		// http://dev.w3.org/html5/html-author/charref
		FileReader reader = new FileReader(new File("E:/chars.html"));
		int i = reader.read();
		StringBuilder sb = new StringBuilder();
		List<String> names = new Vector<String>();
		while (i > 0) {
			char c = (char) i;
			if (c == '&') {
				sb = new StringBuilder();
			}
			sb.append(c);
			if (c == ';') {
				if (sb.toString().startsWith("&amp;")) {
					if (sb.length() > 5) {
						if (sb.charAt(5) == '#') {
							if (sb.charAt(6) != 'x') {
								for (String name : names) {
									String key = name.substring(5,
											name.length() - 1);
									String value = sb.toString().substring(6,
											sb.length() - 1);
									System.out.println("mMap.put(\"" + key
											+ "\", " + value + ");");
								}
								names.clear();
							}
						} else {
							names.add(sb.toString());
						}
					}
				}
			}

			i = reader.read();
		}
		reader.close();
	}
